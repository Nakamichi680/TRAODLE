/*------------------------------------------------------------------------------------------------------------------
Converti i radianti in gradi
INPUT: XYZ *rad
OUTPUT: XYZ *rad
------------------------------------------------------------------------------------------------------------------*/

#include "stdafx.h"
#include "Classes.h"


void mathRadToDeg (XYZ *rad)
{
    rad->x *= 180/3.141593f;
    rad->y *= 180/3.141593f;
    rad->z *= 180/3.141593f;
}